      <h2 id="kostnad" class="special-font header-bigger">Kostnad och bindningstid</h2>
      <h3 class="special-font">Prova på</h3>
      <p>Netflix erbjuder en &ldquo;prova på&rdquo; tjänst för alla nya potentiella kunder där man kan
      kan prova på tjänsten i 1 månad. Under denna månad har man fritt tillträde till hela
      utbudet av både film och serier. Om man bestämmer sig för att man vill fortsätta
      vara prenumerant av Netflix behöver man inget göra efter att gratismånaden har upphört.
      Om man däremot inte vill fortsätta efter sin första månad måste man aktivt avsluta
      prenumerationen.</p>
      <h3 class="special-font">Kostnad</h3>
      <p>Netflix har 3 olika abonnemang som innebär lite olika saker. Det första abonnemanget kostar <strong>79kr/månad</strong> och innebär att det går att titta på en skärm åt gången i standardupplösning. &ldquo;Mellanabonnemanget&rdquo; kostar <strong>89kr/månad</strong> och med detta abonnemang kan man titta på 2 skärmar samtidigt i HD kvalité, om ens Internet och TV klarar av det. Det tredje och sista abonnemanget kostar <strong>119kr/månad</strong> där det går att titta på upp till 4 skärmar samtidigt. Utöver HD kvalité har även detta abonnemang stöd för så kallad <a class="link" href="https://sv.wikipedia.org/wiki/Ultra_High_Definition_Television">Ultra HD</a>.</p>
      <p>Alla abonnemangen erbjuder obegränsat med tv-serier och filmer. Det som skiljer abonnemangen åt är alltså hur många enheter som kan streama samtidigt samt den tillgängliga kvaliteten. Som potentiell prenumerant är det alltså viktigt att ta sig en funderade och se över ens egna behov eller sin familjs behov.</p>
      <h3 class="special-font">Bindningstid</h3>
      <p>Om man väljer att vara Netflix-prenumerant faktureras man månadsvis men helt utan bindningstid. En prenumerant kan när som helst välja att avsluta sin prenumeration.</p>

      <h2 id="streamare" class="special-font header-bigger space-above">Aktiva Streamare</h2>
      <p>Som redan beskrivet under rubriken <a class="link" href="#kostnad">Kostnad</a> avgör vilken typ av abonnemang man har hur många som kan streama innehåll från Netflix samtidigt. Kort sagt handlar det om att antingen 1, 2 eller 4 skärmar kan strömma innehåll från Netflix samtidigt.</p>

      <h2 id="individanpassning" class="special-font header-bigger space-above">Individanpassning</h2>
      <p>Netflix möjligör individanpassning på en rad olika smarta sätt, allt för att varje enskild användare snabbt ska komma åt innehåll som passar individen. Med hjälp av profiler, listor och anpassade förslag försöker Netflix anpassa upplevelsen på bästa möjliga sätt.</p>
      <h3 id="profiler" class="special-font">Profiler</h3>
      <p>Med profiler menar Netflix att olika medlemmar i exempelvis ett hushåll kan ha en egen, anpassad Netflix-upplevelse som baseras på filmerna och tv-serierna de gillar. Det går att skapa upp till 5 olika profiler där profilerna kan döpas om och få bilder, så kallade avatarer.  Profilerna är själva basen i hur Netflix individanpassar sitt innehåll där listor och anpassade förslag baserar sig på vilken profil som är aktiv.</p>
      <h3 class="special-font">Min lista</h3>
      <p>Då Netflix innehållskatalog är så pass stor är risken stor att man tappar bort sig i innehållet. Låt säga att du bläddrar i innehållet och letar efter grymma action-filmer. Du är ute efter en film med Sylvester Stallone men hittar en film med Arnold Schwarzenegger som verkar grym. Nu var du ju ute efter en film med Stallone men vet att du kommer vilja se den andra filmen någon annan gång. Du är dock rädd för att du inte kommer komma ihåg vad filmen med Schwarzenegger heter eller att du glömmer bort att den ens finns. Det du då kan göra är att lägga till Schwarzenegger-filmen i din lista. Schwarzenegger-filmen kommer då att finnas i din lista tills det att du väljer att ta bort den därifrån igen. Listan är alltså ett kraftfultl sätt att spara filmer och tv-serier som du är intresserade av.</p>
      <p>Listan är anpassad efter profilerna och när exempelvis Schwarzenegger-filmen läggs till i listan för en profil läggs den inte till de andra profilernas listor.</p>
      <h3 class="special-font">Anpassade förslag för filmer och tv-serier</h3>
      <p>För att bygga vidare på exemplet om action-filmer med Sylvester Stallone kommer Netflix att rekommendera och ge förslag på liknande filmer efter att du har sätt filmen med Stallone. Låt säga att Stallone-filmen var <a class="link" href="http://www.imdb.com/title/tt0083944/?ref_=tt_rec_tt">Rambo: First Blood</a>. Det Netflix då gör är att ge dig förslag på liknande filmer och exempelvis skulle du via Netflix förslag bli tipsad om uppföljaren, <a class="link" href="http://www.imdb.com/title/tt0089880/?ref_=tt_rec_tt">Rambo: FIrst Blood II</a>. Detta då förutsatt att Netflix har bägge filmerna i sitt sortiment.</p>
      <p>Tanken med anpassade förslag är att ju mer filmer och tv-serier man tittar på via en profil ju bättre blir Netflix på att förstå din smak och kan ge tips därefter.</p>
