      <h2 id="inledning" class="special-font header-bigger">Inledning</h2>
      <p>Netflix är ett amerikanskt företag som startade redan 1997 och har lyckats anpassa sig för den digitala efterfrågan av streaming. År 2012 lanserades tjänsten i norden. Streamingutbudet som består av filmer, tv-serier och till och med egenproducerat material. Utbudet varierar beroende på vilket land man befinner sig i, men detta gäller dock inte det egenproducerade innehållet. Tjänsten är tillgänglig på en uppsjö olika plattformar och beroende på vilken typ av månadskostnad man betalar kan olika antal personer streama samtidigt</p>

      <h2 id="utbud" class="special-font header-bigger space-above">Utbud</h2>
      <h3 class="special-font">Film</h3>
      <p>1000-tals filmer finns tillgängligt att streama från Netflix. Allt från krigsfilmer, komedier, skräck och indiefilmer finns att titta på.</p>
      <p>När det finns så pass många filmer till ens förfogande kan det lätt bli överväldigande. Netflix försöker råda bot på detta genom att det går att kategorisera urvalet väl. Låt säga att du är sugen på att se en satir av något slag. Att leta igenom 1000-tals filmer för att hitta en satir är då inte ett speciellt effektivt sätt att hitta rätt film. De man då kan göra är att bläddra med hjälp av genrer och undergenrer för att sålla bort alla filmer som inte är satirer, i det här fallet. Netflix har alltså tagit fram verktyg för att en användare snabbt ska kunna hitta rätt.</p>
      <h3 class="special-font">Tv-Serier</h3>
      <p>Även utbudet för tv-serier är väldigt stort och samma filtrering och sökmöjligheter finns att använda sig av för att hitta intressanta tv-serier, precis som med filmer.</p>
      <p>Något Netflix har börjat göra allt mer och mer är att göra egna tv-serier av väldigt hög kvalité som i många fall tas emot väldigt bra av sina användare och det skulle inte förvåna mig om många prenumeranter är just det för att Netflix egenproducerade innehåll är så pass bra.</p>
      <h4 lang="en" class="special-font">Binge-watching</h4>
