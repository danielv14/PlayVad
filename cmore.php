      <h4 class="special-font">C More Sport</h4>
      <p>Detta är ett abonnemang som kostar <strong>299kr/månad</strong> och är ett abonnemang som riktar sig till de som vill se på sport av alla möjliga slag. Se mer <a  class="link" href="#utbud">här</a> om sportutbudet.</p>
