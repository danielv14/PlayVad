      <h2 class="special-font">Prova på</h2>
      <p>Viaplay erbjudar alla potenitella nya kunder en månads &ldquo;prova på&rdquo; period helt gratis. När första månaden är slut behövs ingenting göras för att prenumerationen ska rulla på men om man däremot inte vill fortsätta prenumerationen efter första månaden är slut måste man aktivt stänga av sitt konto.</p>
      <h2 class="special-font">Kostnad</h2>
      <p>Viaplay har två olika abonnemang som kostar antingen  <b>99kr/månad</b> eller <b>299kr/månad</b>. Beroende på om man vill att sport ska ingå i sitt abonnemang eller ej stiger priset. Det som är gemensamt för abonnemangen är &ldquo;grundutbudet&rdquo; av film och tv-serier. Är man alltså inte intresserad av att titta på sport genom Viaplay finns det ingen anledning att abonnera det dyrare alternativet då den enda skillnaden är att sportutbud tillkommer.</p>
      <h2 class="special-font">Bindningstid</h2>
      <p>Bindningstid är ett koncept inte Viaplay inte använder sig av utan en prenumerant kan när som helst under prenumerationstiden välja att avlsuta sin prenumeration. Man betalar då bara för den aktiva månaden och inget mer därefter.</p>

      <h1 id="streamare" class="special-font header-bigger space-above">Aktiva Streamare</h1>
      <p>Med Viaplay går det att ha två skärmar igång samtidigt. Det innebär alltså att exempelvis en person kan titta på en film medan en annan person kan kolla på senaste avsnittet av sin favorit tv-serie samtidigt.</p>
      <p>Värt at påpeka är att Viaplay endast tillåter att upp till 4 stycken enheter är registrerade på ett konto samtidigt. Alltså är det upp till 4 stycken enheter som kan strömma max 2 olika saker (filmer, tv-serier och sport) samtidigt.</p>
