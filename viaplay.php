      <p>Viaplay sammarbetar med bland annat Warner Brothers, Walt Disney, Fox, Sony Pictures och Svensk Filmindustri för att kunna leverara bra innehåll till sina kunder.</p>
      <h2 class="special-font">Film</h2>
      <p>Viaplay erbjuder prenumeranter tusentals filmer inom en rad olika genrer såsom rysare, skräck, nodiskt och action. Kort sagt finns det förhoppningsvis något för alla när det gäller Viaplays filmutbud.</p>
      <p>Med effektiva filtrering och kategoriseringsmöjligheter är det enkelt att få sig en överblick om filmutbudet. Att Viaplay visar IMDb-betyg från hemsidan <a class="link" href="http://www.imdb.com">IMDb</a> knutet till varje film innebär att man som användare snabbt kan bilda sig en uppfattning om vad allmänheten tycker om en film. Att det dessutom går att sortera filmer efter högst betyg men även årtal, populära och senast tillagda gör att användaren har flera olika sätt att hitta nya filmer på.</p>
      <h2 class="special-font">Tv-Serier</h2>
      <p>Amerikanska toppserier, realityprogram och svenska tv-serier är bland annat det som utgör Viaplays utbud av tv-serier. Precis som med filmerna visas alla tv-serier med tillhörande IMDb-betyg för att användare ska bilda sig en uppfattning om allmänhetens uppfattning om en tv-serie. Utbudet går att filtrera och sortera på en rad olika sätt däribland efter senast tillagda och efter högst betyg.</p>
      <p>Tv-serier inom exempelvis genrena action, kriminaldrama, komedi och reality finns att konsumera, däribland exempelvis den klassiska kultserien Seinfeld och svenska Paradise Hotel. Med sitt breda utbud finns det något för alla att titta på, oavsett smak.</p>
      <h2 class="special-font">Barn</h2>
      <p>Viaplay är bra på att separera det ordinarie innehållet och innehållet som riktar sig till barn. Med innehåll från bland annat Disney, Nickelodeon, Cartoon Network finns det något för alla barn oavsett ålder. Barnkateogorin innehåller både filmer och tv-serier där det går att filtrera innehållet efter ett barns ålder. Om exempelvis ett barn är mellan 0-3 år går det att enkelt filtrera innehåll efter detta.</p>
